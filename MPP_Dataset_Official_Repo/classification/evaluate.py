import argparse
import csv
import sys
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
import seaborn as sns
import torch
from matplotlib import cm as mpl_cm
from matplotlib import colors as mpl_colors
from sklearn.metrics import (
    accuracy_score,
    confusion_matrix,
    precision_recall_fscore_support,
)
from torch.utils.data import DataLoader

PROJECT_DIR = Path(__file__).resolve().parent
if str(PROJECT_DIR) not in sys.path:
    sys.path.insert(0, str(PROJECT_DIR))

from dataset import CTSliceDataset, TaskName
from models import ModelName, create_model


@torch.no_grad()
def predict(
    model: torch.nn.Module,
    loader: DataLoader,
    device: torch.device,
) -> tuple[list[int], list[int]]:
    model.eval()
    all_y: list[int] = []
    all_pred: list[int] = []
    for x, y in loader:
        x = x.to(device, non_blocking=True)
        logits = model(x)
        pred = torch.argmax(logits, dim=1).detach().cpu().tolist()
        all_pred.extend(pred)
        all_y.extend(y.tolist())
    return all_y, all_pred


def compute_metrics(y_true: list[int], y_pred: list[int]) -> dict[str, float]:
    acc = accuracy_score(y_true, y_pred)
    p, r, f1, _ = precision_recall_fscore_support(y_true, y_pred, average="macro", zero_division=0)
    return {"accuracy": float(acc), "precision_macro": float(p), "recall_macro": float(r), "f1_macro": float(f1)}


def write_results_table2(results_csv: Path, rows: list[dict[str, object]], overwrite: bool) -> None:
    results_csv.parent.mkdir(parents=True, exist_ok=True)
    mode = "w" if overwrite else "a"
    file_exists = results_csv.exists()
    write_header = overwrite or not file_exists
    fieldnames = [
        "task",
        "model",
        "split",
        "accuracy",
        "precision_macro",
        "recall_macro",
        "f1_macro",
        "checkpoint_path",
    ]
    with results_csv.open(mode, newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        if write_header:
            w.writeheader()
        for row in rows:
            w.writerow({k: row.get(k, "") for k in fieldnames})


def plot_and_save_confusion_matrix(
    cm: np.ndarray,
    classes: list[str],
    title: str,
    out_path: Path,
    cmap: str,
    normalized: bool,
) -> None:
    out_path.parent.mkdir(parents=True, exist_ok=True)
    plt.figure(figsize=(7, 6))
    fmt = ".2f" if normalized else "d"
    sns.heatmap(
        cm,
        annot=True,
        fmt=fmt,
        cmap=cmap,
        vmin=0.0 if normalized else None,
        vmax=1.0 if normalized else None,
        xticklabels=classes,
        yticklabels=classes,
    )
    plt.xlabel("Predicted")
    plt.ylabel("True")
    plt.title(title)
    plt.tight_layout()
    plt.savefig(out_path, dpi=300)
    plt.close()


def normalize_confusion_matrix(cm: np.ndarray) -> np.ndarray:
    cm = cm.astype(np.float64)
    row_sum = cm.sum(axis=1, keepdims=True)
    out = np.zeros_like(cm, dtype=np.float64)
    np.divide(cm, row_sum, out=out, where=row_sum != 0)
    return out


def plot_confusion_grid(
    cms: list[np.ndarray],
    model_names: list[str],
    classes: list[str],
    out_path: Path,
    cmap: str,
) -> None:
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig, axes = plt.subplots(2, 2, figsize=(12, 10))
    axes_flat = axes.flatten()
    for i, ax in enumerate(axes_flat):
        if i >= len(cms):
            ax.axis("off")
            continue
        sns.heatmap(
            cms[i],
            annot=True,
            fmt=".2f",
            cmap=cmap,
            vmin=0.0,
            vmax=1.0,
            xticklabels=classes,
            yticklabels=classes,
            cbar=False,
            ax=ax,
        )
        ax.set_xlabel("Predicted")
        ax.set_ylabel("True")
        ax.set_title(model_names[i])
        ax.tick_params(axis="x", rotation=45)
        ax.tick_params(axis="y", rotation=0)

    mappable = mpl_cm.ScalarMappable(norm=mpl_colors.Normalize(vmin=0.0, vmax=1.0), cmap=cmap)
    fig.colorbar(mappable, ax=axes_flat.tolist(), fraction=0.046, pad=0.04)
    fig.tight_layout()
    fig.savefig(out_path, dpi=300)
    plt.close(fig)


def load_checkpoint_path(out_dir: Path, task: TaskName, model_name: ModelName) -> Path:
    ckpt = out_dir / "checkpoints" / f"task_{task}" / model_name / "best.pt"
    if not ckpt.exists():
        raise FileNotFoundError(f"Checkpoint not found: {ckpt}")
    return ckpt


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--data_root", type=str, required=True, help="Dataset root containing class/series PNG folders.")
    p.add_argument("--split_csv", type=str, required=True, help="CSV generated by classification/data_split.py.")
    p.add_argument("--task", type=str, choices=["A", "B", "C"], default="C")
    p.add_argument("--models", type=str, default="vit_b16,resnet50,convnext_tiny,swin_tiny")
    p.add_argument("--batch_size", type=int, default=64)
    p.add_argument("--num_workers", type=int, default=4)
    p.add_argument("--out_dir", type=str, default=str(Path(__file__).resolve().parent / "outputs"))
    p.add_argument("--overwrite_results", action="store_true")
    p.add_argument("--no_confusion_matrix", action="store_true")
    p.add_argument("--cmap", type=str, default="Blues")
    args = p.parse_args()

    if not torch.cuda.is_available():
        raise RuntimeError(
            "CUDA is required for evaluation in this project setup.\n"
            f"Detected torch={torch.__version__}, torch.version.cuda={getattr(torch.version, 'cuda', None)}.\n"
            "Please install a CUDA-enabled PyTorch build, then re-run."
        )

    device = torch.device("cuda")
    task: TaskName = args.task  # type: ignore[assignment]
    out_dir = Path(args.out_dir)
    results_csv = out_dir / "results_table2.csv"
    generate_cm = not bool(args.no_confusion_matrix)

    test_ds = CTSliceDataset(args.data_root, args.split_csv, split="test", task=task)
    test_loader = DataLoader(
        test_ds,
        batch_size=args.batch_size,
        shuffle=False,
        num_workers=args.num_workers,
        pin_memory=True,
        drop_last=False,
    )

    model_names: list[ModelName] = [m.strip() for m in args.models.split(",") if m.strip()]  # type: ignore[assignment]
    rows: list[dict[str, object]] = []
    cms_norm: list[np.ndarray] = []
    cm_labels: list[str] | None = None

    if task == "A":
        expected = ["Normal_MPP", "Severe_SMPP"]
        if list(test_ds.classes) != expected:
            raise RuntimeError(f"Task A class order mismatch. Expected {expected}, got {list(test_ds.classes)}")
        display_labels = expected
    elif task == "B":
        expected = ["MPP", "HH", "Viral"]
        if list(test_ds.classes) != expected:
            raise RuntimeError(f"Task B class order mismatch. Expected {expected}, got {list(test_ds.classes)}")
        display_labels = expected
    else:
        expected_internal = ["HH", "MPP", "SMPP", "Viral"]
        if list(test_ds.classes) != expected_internal:
            raise RuntimeError(f"Task C class order mismatch. Expected {expected_internal}, got {list(test_ds.classes)}")
        display_labels = ["HH", "Normal_MPP", "Severe_SMPP", "Viral"]

    for model_name in model_names:
        ckpt_path = load_checkpoint_path(out_dir, task=task, model_name=model_name)
        ckpt = torch.load(ckpt_path, map_location=device)
        classes = list(ckpt.get("classes", test_ds.classes))
        if task != "C" and classes != list(test_ds.classes):
            raise RuntimeError(f"Checkpoint classes mismatch for {model_name}: {classes}")
        if task == "C" and classes != ["HH", "MPP", "SMPP", "Viral"]:
            raise RuntimeError(f"Checkpoint classes mismatch for {model_name}: {classes}")
        num_classes = len(classes)

        model = create_model(model_name, num_classes=num_classes, pretrained=False, device=device)
        model.load_state_dict(ckpt["model_state"])

        y_true, y_pred = predict(model, test_loader, device=device)
        metrics = compute_metrics(y_true, y_pred)
        print(
            f"{task=} {model_name=} | acc={metrics['accuracy']:.4f} | "
            f"precision={metrics['precision_macro']:.4f} | recall={metrics['recall_macro']:.4f} | f1={metrics['f1_macro']:.4f}"
        )
        rows.append(
            {
                "task": task,
                "model": model_name,
                "split": "test",
                "accuracy": metrics["accuracy"],
                "precision_macro": metrics["precision_macro"],
                "recall_macro": metrics["recall_macro"],
                "f1_macro": metrics["f1_macro"],
                "checkpoint_path": str(ckpt_path.resolve()),
            }
        )

        if generate_cm:
            cm = confusion_matrix(y_true, y_pred, labels=list(range(num_classes)))
            cm_norm = normalize_confusion_matrix(cm)
            cm_labels = display_labels
            out_path = out_dir / "confusion_matrices" / f"task_{task}" / f"{model_name}_normalized.png"
            plot_and_save_confusion_matrix(
                cm_norm,
                classes=display_labels,
                title=f"Task {task} Normalized Confusion Matrix ({model_name})",
                out_path=out_path,
                cmap=args.cmap,
                normalized=True,
            )
            print(f"Saved confusion matrix: {out_path}")
            cms_norm.append(cm_norm)

    write_results_table2(results_csv, rows, overwrite=args.overwrite_results)
    print(f"Wrote/updated: {results_csv}")

    if generate_cm and cms_norm:
        grid_path = out_dir / "confusion_matrices" / f"task_{task}" / "normalized_grid.png"
        plot_confusion_grid(cms_norm, [str(m) for m in model_names], classes=cm_labels or display_labels, out_path=grid_path, cmap=args.cmap)
        print(f"Saved confusion matrix grid: {grid_path}")


if __name__ == "__main__":
    main()
