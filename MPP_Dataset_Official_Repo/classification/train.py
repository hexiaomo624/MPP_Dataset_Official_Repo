import argparse
import csv
import json
import os
import random
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Literal

import numpy as np
import torch
import torch.nn.functional as F
from sklearn.metrics import accuracy_score, precision_recall_fscore_support
from torch.cuda.amp import GradScaler, autocast
from torch.utils.data import DataLoader, WeightedRandomSampler

PROJECT_DIR = Path(__file__).resolve().parent
if str(PROJECT_DIR) not in sys.path:
    sys.path.insert(0, str(PROJECT_DIR))

from dataset import CTSliceDataset, TaskName
from models import ModelName, create_model


SplitName = Literal["train", "val", "test"]


def seed_everything(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)
    torch.backends.cudnn.deterministic = False
    torch.backends.cudnn.benchmark = True


def compute_sampler(labels: list[int], num_classes: int) -> WeightedRandomSampler:
    counts = np.zeros(num_classes, dtype=np.int64)
    for y in labels:
        counts[y] += 1
    counts = np.maximum(counts, 1)
    weights = np.array([1.0 / counts[y] for y in labels], dtype=np.float64)
    return WeightedRandomSampler(weights=torch.as_tensor(weights, dtype=torch.double), num_samples=len(weights), replacement=True)


def compute_ce_weights(labels: list[int], num_classes: int) -> torch.Tensor:
    counts = np.zeros(num_classes, dtype=np.int64)
    for y in labels:
        counts[y] += 1
    counts = np.maximum(counts, 1)
    inv = 1.0 / counts.astype(np.float64)
    w = inv / inv.sum() * num_classes
    return torch.as_tensor(w, dtype=torch.float32)


@dataclass
class EarlyStopping:
    patience: int
    min_delta: float = 0.0

    def __post_init__(self) -> None:
        self.best: float | None = None
        self.bad_epochs = 0

    def step(self, current: float) -> bool:
        if self.best is None or current < self.best - self.min_delta:
            self.best = current
            self.bad_epochs = 0
            return False
        self.bad_epochs += 1
        return self.bad_epochs >= self.patience


@torch.no_grad()
def evaluate(
    model: torch.nn.Module,
    loader: DataLoader,
    device: torch.device,
) -> tuple[float, dict[str, float]]:
    model.eval()
    losses: list[float] = []
    all_y: list[int] = []
    all_pred: list[int] = []

    for batch in loader:
        x, y = batch
        x = x.to(device, non_blocking=True)
        y = y.to(device, non_blocking=True)
        logits = model(x)
        loss = F.cross_entropy(logits, y)
        losses.append(float(loss.detach().cpu().item()))
        pred = torch.argmax(logits, dim=1)
        all_y.extend(y.detach().cpu().tolist())
        all_pred.extend(pred.detach().cpu().tolist())

    acc = accuracy_score(all_y, all_pred)
    p, r, f1, _ = precision_recall_fscore_support(all_y, all_pred, average="macro", zero_division=0)
    metrics = {"accuracy": float(acc), "precision_macro": float(p), "recall_macro": float(r), "f1_macro": float(f1)}
    return float(np.mean(losses)), metrics


def append_results_row(results_csv: Path, row: dict[str, object]) -> None:
    results_csv.parent.mkdir(parents=True, exist_ok=True)
    file_exists = results_csv.exists()
    fieldnames = [
        "task",
        "model",
        "split",
        "accuracy",
        "precision_macro",
        "recall_macro",
        "f1_macro",
        "checkpoint_path",
    ]
    with results_csv.open("a", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        if not file_exists:
            w.writeheader()
        w.writerow({k: row.get(k, "") for k in fieldnames})


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--data_root", type=str, required=True, help="Dataset root containing class/series PNG folders.")
    p.add_argument("--split_csv", type=str, required=True, help="CSV generated by classification/data_split.py.")
    p.add_argument("--task", type=str, choices=["A", "B", "C"], default="A")
    p.add_argument("--model", type=str, choices=["vit_b16", "resnet50", "convnext_tiny", "swin_tiny"], default="vit_b16")
    p.add_argument("--epochs", type=int, default=50)
    p.add_argument("--batch_size", type=int, default=64)
    p.add_argument("--lr", type=float, default=1e-4)
    p.add_argument("--weight_decay", type=float, default=1e-2)
    p.add_argument("--num_workers", type=int, default=4)
    p.add_argument("--seed", type=int, default=42)
    p.add_argument("--patience", type=int, default=10)
    p.add_argument("--no_weighted_sampler", action="store_true")
    p.add_argument("--use_ce_weights", action="store_true")
    p.add_argument("--out_dir", type=str, default=str(Path(__file__).resolve().parent / "outputs"))
    args = p.parse_args()

    if not torch.cuda.is_available():
        raise RuntimeError(
            "CUDA is required for this training script.\n"
            f"Detected torch={torch.__version__}, torch.version.cuda={getattr(torch.version, 'cuda', None)}.\n"
            "Please install a CUDA-enabled PyTorch build (and make sure your NVIDIA driver is installed), then re-run."
        )

    os.environ["CUDA_VISIBLE_DEVICES"] = os.environ.get("CUDA_VISIBLE_DEVICES", "0")
    device = torch.device("cuda")
    seed_everything(args.seed)

    task: TaskName = args.task  # type: ignore[assignment]
    model_name: ModelName = args.model  # type: ignore[assignment]

    out_dir = Path(args.out_dir)
    ckpt_dir = out_dir / "checkpoints" / f"task_{task}" / model_name
    ckpt_dir.mkdir(parents=True, exist_ok=True)
    best_ckpt_path = ckpt_dir / "best.pt"
    last_ckpt_path = ckpt_dir / "last.pt"
    results_csv = out_dir / "results_table2.csv"
    run_config_path = ckpt_dir / "run_config.json"

    train_ds = CTSliceDataset(args.data_root, args.split_csv, split="train", task=task)
    val_ds = CTSliceDataset(args.data_root, args.split_csv, split="val", task=task)
    test_ds = CTSliceDataset(args.data_root, args.split_csv, split="test", task=task)

    train_labels = [s.y for s in train_ds.samples]
    num_classes = len(train_ds.classes)

    use_weighted_sampler = not bool(args.no_weighted_sampler)
    sampler = compute_sampler(train_labels, num_classes) if use_weighted_sampler else None
    train_loader = DataLoader(
        train_ds,
        batch_size=args.batch_size,
        shuffle=(sampler is None),
        sampler=sampler,
        num_workers=args.num_workers,
        pin_memory=True,
        drop_last=False,
    )
    val_loader = DataLoader(
        val_ds,
        batch_size=args.batch_size,
        shuffle=False,
        num_workers=args.num_workers,
        pin_memory=True,
        drop_last=False,
    )
    test_loader = DataLoader(
        test_ds,
        batch_size=args.batch_size,
        shuffle=False,
        num_workers=args.num_workers,
        pin_memory=True,
        drop_last=False,
    )

    model = create_model(model_name, num_classes=num_classes, pretrained=True, device=device)
    optimizer = torch.optim.AdamW(model.parameters(), lr=args.lr, weight_decay=args.weight_decay)
    scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode="min", factor=0.5, patience=3)
    scaler = GradScaler()

    ce_weight = compute_ce_weights(train_labels, num_classes).to(device) if args.use_ce_weights else None
    early = EarlyStopping(patience=args.patience)

    with run_config_path.open("w", encoding="utf-8") as f:
        json.dump(
            {
                "data_root": str(Path(args.data_root).resolve()),
                "split_csv": str(Path(args.split_csv).resolve()),
                "task": task,
                "model": model_name,
                "epochs": args.epochs,
                "batch_size": args.batch_size,
                "lr": args.lr,
                "weight_decay": args.weight_decay,
                "num_workers": args.num_workers,
                "seed": args.seed,
                "use_weighted_sampler": use_weighted_sampler,
                "use_ce_weights": bool(args.use_ce_weights),
                "classes": train_ds.classes,
            },
            f,
            ensure_ascii=False,
            indent=2,
        )

    best_val_loss = float("inf")
    for epoch in range(1, args.epochs + 1):
        model.train()
        train_losses: list[float] = []
        for x, y in train_loader:
            x = x.to(device, non_blocking=True)
            y = y.to(device, non_blocking=True)

            optimizer.zero_grad(set_to_none=True)
            with autocast():
                logits = model(x)
                loss = F.cross_entropy(logits, y, weight=ce_weight)
            scaler.scale(loss).backward()
            scaler.step(optimizer)
            scaler.update()
            train_losses.append(float(loss.detach().cpu().item()))

        val_loss, val_metrics = evaluate(model, val_loader, device=device)
        scheduler.step(val_loss)

        mean_train_loss = float(np.mean(train_losses)) if train_losses else float("nan")
        lr = optimizer.param_groups[0]["lr"]
        print(
            f"Epoch {epoch:03d} | lr={lr:.2e} | train_loss={mean_train_loss:.4f} | val_loss={val_loss:.4f} | "
            f"val_acc={val_metrics['accuracy']:.4f} | val_f1={val_metrics['f1_macro']:.4f}"
        )

        torch.save(
            {
                "model_state": model.state_dict(),
                "epoch": epoch,
                "val_loss": val_loss,
                "task": task,
                "model": model_name,
                "classes": train_ds.classes,
            },
            last_ckpt_path,
        )

        if val_loss < best_val_loss:
            best_val_loss = val_loss
            torch.save(
                {
                    "model_state": model.state_dict(),
                    "epoch": epoch,
                    "val_loss": val_loss,
                    "task": task,
                    "model": model_name,
                    "classes": train_ds.classes,
                },
                best_ckpt_path,
            )

        if early.step(val_loss):
            print(f"Early stopping triggered at epoch {epoch}. Best val_loss={best_val_loss:.4f}")
            break

    ckpt = torch.load(best_ckpt_path, map_location=device)
    model.load_state_dict(ckpt["model_state"])
    test_loss, test_metrics = evaluate(model, test_loader, device=device)
    print(f"Test | loss={test_loss:.4f} | acc={test_metrics['accuracy']:.4f} | f1={test_metrics['f1_macro']:.4f}")

    append_results_row(
        results_csv,
        {
            "task": task,
            "model": model_name,
            "split": "test",
            "accuracy": test_metrics["accuracy"],
            "precision_macro": test_metrics["precision_macro"],
            "recall_macro": test_metrics["recall_macro"],
            "f1_macro": test_metrics["f1_macro"],
            "checkpoint_path": str(best_ckpt_path.resolve()),
        },
    )
    print(f"Wrote/updated: {results_csv}")


if __name__ == "__main__":
    main()
