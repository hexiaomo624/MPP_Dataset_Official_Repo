"""Generate a Markdown audit report from dataset_global_stats.json."""

from __future__ import annotations

import argparse
import json
from pathlib import Path


def resolution_range(resolutions: dict[str, int]) -> str:
    widths: list[int] = []
    heights: list[int] = []
    for key in resolutions:
        try:
            w, h = map(int, key.split("x"))
        except ValueError:
            continue
        widths.append(w)
        heights.append(h)
    if not widths:
        return "not available"
    return f"{min(widths)}x{min(heights)} to {max(widths)}x{max(heights)}"


def build_report(data: dict) -> str:
    groups = data.get("groups", {})
    physical = data.get("physical", {})
    intensity = data.get("intensity", {})
    global_stats = data.get("global", {})

    rows = []
    for group, values in groups.items():
        rows.append(
            f"| {group} | {values.get('unique_patients', 0)} | {values.get('total_series', 0)} | {values.get('total_pngs', 0):,} |"
        )

    min_case = physical.get("min_slices_case", {"id": "", "count": 0})
    max_case = physical.get("max_slices_case", {"id": "", "count": 0})
    res_range = resolution_range(physical.get("resolutions", {}))

    return f"""# Dataset Audit Report

## Asset Summary

| Class | Unique patients | Total series | Total PNGs |
| :--- | ---: | ---: | ---: |
{chr(10).join(rows)}
| **Grand total** | **{global_stats.get('unique_patients', 0)}** | **{global_stats.get('total_series', 0)}** | **{global_stats.get('total_pngs', 0):,}** |

## Physical And Image Properties

- Resampling target: 1.0 mm isotropic spacing, generated by the preprocessing pipeline.
- Resolution range after lung-aware cropping: {res_range}.
- Minimum slices per series: {min_case.get('count', 0)} (`{min_case.get('id', '')}`).
- Maximum slices per series: {max_case.get('count', 0)} (`{max_case.get('id', '')}`).
- Empty series folders: {global_stats.get('empty_dirs', 0)}.
- Zero-byte files detected: {global_stats.get('zero_byte_files', 0)}.

## Intensity Statistics

- Global mean after 0-1 scaling: {float(intensity.get('mean', 0.0)):.4f}
- Global std after 0-1 scaling: {float(intensity.get('std', 0.0)):.4f}

## Manuscript-Relevant Processing Notes

- DICOM values are converted to HU using RescaleSlope and RescaleIntercept.
- Volumes are resampled with B-Spline interpolation to 1.0 mm isotropic spacing.
- Lung-window PNGs use WL=-600 HU and WW=1500 HU, then are quantized to 8-bit PNG.
- Slice-thickness exclusion is handled by `data_integrity/filter_slice_thickness.py`.
- Lung-area connected-component audit is handled by `qa_pipeline/lung_area_audit.py`.
- Conservative lightweight cleanup is provided by `qa_pipeline/lightweight_filter.py` as a supplemental tool, not as the direct Otsu implementation.
"""


def build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(description="Generate Markdown report from dataset audit JSON.")
    p.add_argument("--stats-json", required=True)
    p.add_argument("--out-md", default="")
    return p


def main() -> None:
    args = build_parser().parse_args()
    stats_json = Path(args.stats_json)
    data = json.loads(stats_json.read_text(encoding="utf-8"))
    report = build_report(data)
    if args.out_md:
        out_md = Path(args.out_md)
        out_md.parent.mkdir(parents=True, exist_ok=True)
        out_md.write_text(report, encoding="utf-8")
        print(f"Markdown report: {out_md}")
    else:
        print(report)


if __name__ == "__main__":
    main()
